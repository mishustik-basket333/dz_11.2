def funct_upper(str_in):
    """
    Функция принимает строковый аргумент
    и возвращает его же в верхнем регистре
    """
    return(str_in.upper())

def funct_title(str_in):
    """
    Принимает строковый аргумент и возврщает его с заглавными буквами
    :param str_in:
    :return: str_in.title()
    """
    return str_in.title()
